<div class="ps-card">
    <div class="ps-section-title">Betekenis van de waarden</div>
    <div class="ps-section-description">
        Hieronder staat kort uitgelegd wat elke waarde betekent en hoe je ze moet lezen.
    </div>

    <div class="ps-grid-2">
        <div class="ps-explanation-card">
            <div class="ps-explanation-title">Performance</div>
            <div class="ps-explanation-text">
                Algemene Lighthouse-score voor snelheid en laadervaring. Een lage score wijst op een trage of zwaar ladende pagina.
            </div>
        </div>

        <div class="ps-explanation-card">
            <div class="ps-explanation-title">Accessibility</div>
            <div class="ps-explanation-text">
                Geeft aan hoe toegankelijk de pagina is voor gebruikers, bijvoorbeeld met schermlezers, toetsenbordnavigatie en voldoende contrast.
            </div>
        </div>

        <div class="ps-explanation-card">
            <div class="ps-explanation-title">Best Practices</div>
            <div class="ps-explanation-text">
                Controleert algemene technische kwaliteit, veiligheid en moderne webstandaarden.
            </div>
        </div>

        <div class="ps-explanation-card">
            <div class="ps-explanation-title">SEO</div>
            <div class="ps-explanation-text">
                Controleert technische basispunten die invloed kunnen hebben op de vindbaarheid in zoekmachines.
            </div>
        </div>

        <div class="ps-explanation-card">
            <div class="ps-explanation-title">LCP - Largest Contentful Paint</div>
            <div class="ps-explanation-text">
                Meet hoe lang het duurt voordat het grootste zichtbare tekst- of beeldelement geladen is. Lager is beter.
            </div>
        </div>

        <div class="ps-explanation-card">
            <div class="ps-explanation-title">FCP - First Contentful Paint</div>
            <div class="ps-explanation-text">
                Meet hoe lang het duurt voordat de eerste zichtbare inhoud op de pagina verschijnt. Lager is beter.
            </div>
        </div>

        <div class="ps-explanation-card">
            <div class="ps-explanation-title">TBT - Total Blocking Time</div>
            <div class="ps-explanation-text">
                Meet hoe lang de pagina geblokkeerd is tijdens het laden. Een hoge waarde betekent dat de pagina minder snel reageert.
            </div>
        </div>

        <div class="ps-explanation-card">
            <div class="ps-explanation-title">CLS - Cumulative Layout Shift</div>
            <div class="ps-explanation-text">
                Meet hoeveel zichtbare elementen onverwacht verschuiven tijdens het laden. Lager is beter.
            </div>
        </div>

        <div class="ps-explanation-card">
            <div class="ps-explanation-title">Runs gebruikt</div>
            <div class="ps-explanation-text">
                Het aantal Lighthouse-runs dat gebruikt werd om een gemiddelde score te berekenen.
            </div>
        </div>
    </div>

    <div class="ps-explanation-card" style="margin-top: 1rem;">
        <div class="ps-explanation-title">Kleurinterpretatie</div>
        <div class="ps-explanation-text">
            <span style="color: #22c55e; font-weight: 700;">Groen</span> betekent goed,
            <span style="color: #facc15; font-weight: 700;">geel</span> betekent aandachtspunt,
            en <span style="color: #ef4444; font-weight: 700;">rood</span> betekent belangrijk verbeterpunt.
        </div>
    </div>
</div>
